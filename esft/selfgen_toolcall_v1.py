def scaffold_prompt(seed: dict[str, Any], stage: int, tool_results: list[dict[str, Any]]) -> str:
    # This material is input-only scaffolding.  It is never written into train.jsonl.
    return canonical({"system": SYSTEM, "few_shots": FEW_SHOTS, "tools": seed["tools"],
                      "user_request": seed["user_request"], "turn": stage + 1,
                      "prior_tool_results": tool_results,
                      "instruction": "Emit the next assistant tool-call JSON only."})


def render_training(seed: dict[str, Any], selected: list[list[dict[str, Any]]],
                    results: list[list[dict[str, Any]]]) -> dict[str, Any]:
    messages: list[dict[str, Any]] = [{"role": "user", "content": seed["user_request"]}]
    for calls, stage_results in zip(selected, results):
        messages.append({"role": "assistant", "content": None,
                         "tool_calls": [{"type": "function", "function": {
                             "name": call["name"], "arguments": canonical(call["arguments"]),
                         }} for call in calls]})
        for call, result in zip(calls, stage_results):
            messages.append({"role": "tool", "name": call["name"], "content": canonical(result)})
    # Schemas are task context, while system instruction/few-shots are removed.
    return {"tools": seed["tools"], "messages": messages}


def select_candidate(raws: list[str], expected: list[dict[str, Any]], tools: dict[str, dict[str, Any]]) -> tuple[list[dict[str, Any]] | None, list[str], int | None]:
    reasons = []
    for index, raw in enumerate(raws):
        calls, reason = parse_model_turn(raw, tools)
        if reason:
            reasons.append(reason)
            continue
        if canonical(calls) != canonical(expected):
            reasons.append("plan_alignment")
            continue
        return calls, reasons, index
    return None, reasons or ["no_candidate"], None


@dataclass
class GenerationSpec:
    seed: int
    temperature: float
    max_new: int
    best_of: int
    gpu: int


def load_stock_model(gpu: int):
    import torch
    sys.path.insert(0, str(ESFT))
    from eval_harness import resolve_model_spec, load_subject_model
    spec = resolve_model_spec("base", model_path=str(MODEL_PATH), topk=8)
    torch.cuda.set_device(gpu)
    tokenizer, model, _ = load_subject_model(spec, gpu)
    return torch, tokenizer, model


def generate_candidates(torch: Any, tokenizer: Any, model: Any, prompt: str,
                        spec: GenerationSpec) -> list[str]:
    enc = tokenizer(prompt, return_tensors="pt", add_special_tokens=False).to(f"cuda:{spec.gpu}")
    input_len = enc["input_ids"].shape[1]
    with torch.no_grad():
        generated = model.generate(**enc, max_new_tokens=spec.max_new, do_sample=True,
                                   temperature=spec.temperature, top_p=0.95,
                                   num_return_sequences=spec.best_of,
                                   pad_token_id=tokenizer.pad_token_id)
    return [tokenizer.decode(row[input_len:], skip_special_tokens=True) for row in generated]


def checkpoint_path(target: Path, gpu: int) -> Path:
    return target / f"generation_records_gpu{gpu}.jsonl"


def append_checkpoint_record(path: Path, record: dict[str, Any]) -> None:
    """Durably commit one finished task before considering it resumable."""
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("a", encoding="utf-8") as fh:
        fh.write(canonical(record) + "\n")
        fh.flush()
        os.fsync(fh.fileno())


def load_checkpoint_records(target: Path, seeds: list[dict[str, Any]]) -> dict[str, dict[str, Any]]:
    """Load and validate durable task checkpoints from a prior interrupted execute."""
    expected = {seed["seed_id"]: seed for seed in seeds}
    if len(expected) != len(seeds):
        raise RuntimeError("prepared seeds contain duplicate seed IDs")
    records: dict[str, dict[str, Any]] = {}
    for gpu in (0, 1):
        path = checkpoint_path(target, gpu)
        if not path.exists():
            continue
        with path.open(encoding="utf-8") as fh:
            for line_number, line in enumerate(fh, 1):
                if not line.strip():
                    continue
                try:
                    record = json.loads(line)
                    seed = record["seed"]
                    seed_id = seed["seed_id"]
                except (json.JSONDecodeError, KeyError, TypeError) as exc:
                    raise RuntimeError(f"invalid checkpoint {path.name}:{line_number}: {exc}") from exc
                if seed_id not in expected:
                    raise RuntimeError(f"checkpoint {path.name}:{line_number} has unknown task ID {seed_id!r}")
                if canonical(seed) != canonical(expected[seed_id]):
                    raise RuntimeError(f"checkpoint {path.name}:{line_number} does not match frozen seed {seed_id!r}")
                if seed_id in records:
                    raise RuntimeError(f"duplicate completed task ID {seed_id!r} in checkpoints")
                records[seed_id] = record
    return records


def pending_seeds(seeds: list[dict[str, Any]], completed: dict[str, dict[str, Any]]) -> list[dict[str, Any]]:
    return [seed for seed in seeds if seed["seed_id"] not in completed]


def generation_worker(gpu: int, seeds: list[dict[str, Any]], spec: GenerationSpec,
                      checkpoint: Path, output: mp.Queue) -> None:
    try:
        torch, tokenizer, model = load_stock_model(gpu)
        for num, seed in enumerate(seeds, 1):
            tool_map = {tool["name"]: tool for tool in seed["tools"]}
            selected, all_results, selections, failures = [], [], [], []
            prior_results: list[dict[str, Any]] = []
            for stage, expected in enumerate(seed["expected_stages"]):
                raws = generate_candidates(torch, tokenizer, model, scaffold_prompt(seed, stage, prior_results), spec)
                calls, reasons, chosen = select_candidate(raws, expected, tool_map)
                if calls is None:
                    failures.extend(reasons)
                    # Diagnostics only: keep the raw candidates of a failed stage so the
                    # reject reason can be inspected post hoc. Off by default.
                    if os.environ.get("SELFGEN_DEBUG_RAW") == "1":
                        debug = checkpoint.parent / f"debug_failures_gpu{gpu}.jsonl"
                        with debug.open("a", encoding="utf-8") as fh:
                            fh.write(json.dumps(
                                {"seed_id": seed["seed_id"], "pattern": seed["pattern"],
                                 "stage": stage, "reasons": reasons, "raws": raws},
                                ensure_ascii=False) + "\n")
                    break
                results = [mock_execute(call, stage, seed["pattern"]) for call in calls]
                selected.append(calls)
                all_results.append(results)
                selections.append({"stage": stage, "candidate_index": chosen,
                                   "candidate_rejections": reasons})
                prior_results.extend(results)
            record = {"seed": seed, "selected": selected, "results": all_results,
                      "selection": selections, "failures": failures}
            append_checkpoint_record(checkpoint, record)
            print(f"[selfgen gpu{gpu}] {num}/{len(seeds)}", flush=True)
        output.put({"kind": "complete", "gpu": gpu, "records_written": len(seeds)})
    except BaseException as exc:
        output.put({"kind": "error", "gpu": gpu,
                    "error": f"{type(exc).__name__}: {exc}", "traceback": traceback.format_exc()})


def wait_for_worker_completion(workers: list[mp.Process], output: mp.Queue) -> None:
    """Wait indefinitely for terminal worker sentinels while still detecting dead workers."""
    pending = {worker.pid: worker for worker in workers}
    gpu_by_pid = {worker.pid: gpu for gpu, worker in zip((0, 1), workers)}
    last_progress = time.monotonic()
    dead_since: dict[int | None, float] = {}
    while pending:
        try:
            item = output.get_nowait()
        except queue.Empty:
            now = time.monotonic()
            dead = []
            for pid, worker in pending.items():
                if worker.is_alive():
                    dead_since.pop(pid, None)
                    continue
                dead_since.setdefault(pid, now)
                dead.append((pid, gpu_by_pid[pid], worker.exitcode))
            if dead and all(now - dead_since[pid] >= WORKER_SENTINEL_GRACE_SECONDS
                            for pid, _, _ in dead):
                details = ", ".join(f"gpu{gpu} exitcode={exitcode}" for _, gpu, exitcode in dead)
                all_workers_dead = len(dead) == len(pending)
                print(f"[selfgen] worker terminal sentinel missing ({details}; "
                      f"all_workers_dead={all_workers_dead})", flush=True)
                raise RuntimeError(f"generation worker exited without reporting completion: {details}")
            if now - last_progress >= NO_PROGRESS_WARNING_SECONDS:
                live = [f"gpu{gpu_by_pid[pid]} pid={worker.pid}" for pid, worker in pending.items()
                        if worker.is_alive()]
                print(f"[selfgen] no worker terminal progress for "
                      f"{int(now - last_progress)}s; waiting for {', '.join(live) or 'none'}", flush=True)
                last_progress = now
            time.sleep(WORKER_POLL_SECONDS)
            continue
        last_progress = time.monotonic()
        if not isinstance(item, dict) or "gpu" not in item:
            raise RuntimeError(f"invalid generation worker sentinel: {item!r}")
        gpu = item["gpu"]
        matching = [pid for pid, expected_gpu in gpu_by_pid.items() if expected_gpu == gpu]
        if len(matching) != 1 or matching[0] not in pending:
            raise RuntimeError(f"unexpected or duplicate generation worker sentinel for GPU {gpu!r}")
        pid = matching[0]
        if item.get("kind") == "error":
            detail = item.get("error", "unknown worker error")
            worker_traceback = item.get("traceback", "")
            print(f"[selfgen] GPU {gpu} worker exception: {detail}\n{worker_traceback}", flush=True)
            raise RuntimeError(f"GPU {gpu} worker failed: {detail}")
        if item.get("kind") != "complete":
            raise RuntimeError(f"invalid generation worker sentinel for GPU {gpu}: {item!r}")
        del pending[pid]


def cpu_fixture_records(seeds: list[dict[str, Any]]) -> list[dict[str, Any]]:
    """Deterministic validator fixture, explicitly not a model-generated corpus."""
    records = []
    for seed in seeds:
        selected = copy.deepcopy(seed["expected_stages"])
        results = [[mock_execute(call, stage, seed["pattern"]) for call in calls]
                   for stage, calls in enumerate(selected)]
        records.append({"seed": seed, "selected": selected, "results": results,
                        "selection": [{"stage": i, "candidate_index": 0,
                                       "candidate_rejections": []} for i in range(len(selected))],
                        "failures": []})
    return records


def evaluate_records(records: list[dict[str, Any]], eval_grams: set[tuple[str, ...]],
                     eval_names: set[str]) -> tuple[list[dict[str, Any]], list[dict[str, Any]], Counter[str]]:
    accepted, rejected, reasons = [], [], Counter()
    for record in records:
        seed = record["seed"]
        failure = contaminated(seed, eval_grams, eval_names)
        if not failure and record["failures"]:
            failure = record["failures"][0]
        tool_map = {tool["name"]: tool for tool in seed["tools"]}
        if not failure:
            if len(record["selected"]) != len(seed["expected_stages"]):
                failure = "incomplete_dialogue"
            else:
                for stage, (calls, expected, stage_results) in enumerate(zip(
                        record["selected"], seed["expected_stages"], record["results"])):
                    if canonical(calls) != canonical(expected):
                        failure = "plan_alignment"
                        break
                    if len(calls) != len(stage_results):
                        failure = "executor_cardinality"
                        break
                    for call, result in zip(calls, stage_results):
                        err, _ = validate_call(call, tool_map)
                        if err:
                            failure = err
                            break
                        if result != mock_execute(call, stage, seed["pattern"]):
                            failure = "executor_inconsistent"
                            break
                    if not failure and stage > 0:
                        prior = record["results"][stage - 1][0]
                        if seed["pattern"] == "multi_turn":
                            if calls[0]["arguments"].get("prior_receipt") != prior.get("result", {}).get("receipt"):
                                failure = "multi_turn_result_mismatch"
                        elif seed["pattern"] == "error_recovery":
                            if calls[0]["arguments"].get("recovery_code") != prior.get("error", {}).get("code"):
                                failure = "error_recovery_mismatch"
                    if failure:
                        break
        meta = {"id": seed["seed_id"], "domain": seed["domain"], "pattern": seed["pattern"],
                "schema_sha256": hashlib.sha256(canonical(seed["tools"]).encode()).hexdigest(),
                "best_of": 4, "selection": record["selection"],
                "validator": {"json": True, "schema": not failure, "executor": not failure,
                              "multi_turn": not failure, "contamination": not failure}}
        if failure:
            reasons[failure] += 1
            rejected.append({"id": seed["seed_id"], "reason": failure, "seed": seed,
                             "selection": record["selection"]})
        else:
            accepted.append({**render_training(seed, record["selected"], record["results"]), "metadata": meta})
    return accepted, rejected, reasons


def run_dir(run_id: str) -> Path:
    if not re.fullmatch(r"[A-Za-z0-9][A-Za-z0-9_.-]*", run_id):
        raise ValueError("run_id must be safe filename characters")
    return OUT_ROOT / run_id


def verified_stock_identity() -> dict[str, Any]:
    """Reuse the campaign fingerprint check without touching any B2 artifact."""
    with (ESFT / "codex_harness.toml").open("rb") as fh:
        cfg = tomllib.load(fh)
    stock = cfg.get("stock", {})
    if stock.get("revision") != STOCK_REVISION or stock.get("path") != str(MODEL_PATH):
        raise RuntimeError("codex_harness stock config is not the declared true-stock snapshot")
    sys.path.insert(0, str(ESFT))
    from codex_harness import stock_identity
    return stock_identity(stock)


def gpu_preflight(run_id: str) -> dict[str, Any]:
    """Fail closed before CUDA loading; only physical GPU 0/1 are eligible."""
    target = run_dir(run_id)
    manifest = json.loads((target / "manifest.json").read_text(encoding="utf-8"))
    if manifest.get("state") != "prepared":
        raise RuntimeError(f"run state must be prepared, got {manifest.get('state')!r}")
    if manifest.get("model") != {"identity": verified_stock_identity(), "topk": 8, "patch": None}:
        raise RuntimeError("true-stock identity changed since preparation")
    proc = subprocess.run(
        ["nvidia-smi", "--query-gpu=index,name,memory.used,utilization.gpu", "--format=csv,noheader,nounits"],
        text=True, stdout=subprocess.PIPE, stderr=subprocess.STDOUT, check=False)
    if proc.returncode != 0:
        raise RuntimeError(f"GPU preflight failed: nvidia-smi exit {proc.returncode}: {proc.stdout.strip()}")
    rows = []
    for line in proc.stdout.splitlines():
        parts = [x.strip() for x in line.split(",")]
        if len(parts) != 4:
            raise RuntimeError(f"unexpected nvidia-smi row: {line!r}")
        rows.append({"index": int(parts[0]), "name": parts[1], "memory_used_mib": int(parts[2]),
                     "utilization_percent": int(parts[3])})
    by_id = {row["index"]: row for row in rows}
    if not {0, 1}.issubset(by_id) or 2 not in by_id:
        raise RuntimeError("expected local physical GPUs 0, 1, and display GPU 2")
    busy = [by_id[i] for i in (0, 1)
            if by_id[i]["memory_used_mib"] > 1024 or by_id[i]["utilization_percent"] > 5]
    if busy:
        raise RuntimeError(f"GPU 0/1 are busy: {busy}")
    result = {"checked_at": dt.datetime.now(dt.UTC).isoformat(), "run_id": run_id,
              "allowed_physical_gpus": [0, 1], "forbidden_display_gpu": 2, "gpus": rows,
              "stock": manifest["model"]["identity"], "result": "PASS"}
    atomic_json(target / f"preflight_{dt.datetime.now(dt.UTC).strftime('%Y%m%dT%H%M%SZ')}.json", result)
    return result


def prepare(args: argparse.Namespace) -> None:
    target = run_dir(args.run_id)
    if target.exists():
        raise FileExistsError(f"refusing to overwrite {target}")
    stock = verified_stock_identity()
    profile = bfcl_structural_profile()
    contamination, eval_grams, eval_names = contamination_corpus()
    seeds, rejected = build_seeds(args.n, args.seed, profile, eval_grams, eval_names)
    target.mkdir(parents=True)
    atomic_json(target / "seeds.json", {"schema_version": 1, "created_at": dt.datetime.now(dt.UTC).isoformat(),
                                         "rng_seed": args.seed, "count": args.n, "bfcl_structure": profile,
                                         "contamination": contamination, "seed_filter_rejections": dict(rejected),
                                         "seeds": seeds})
    atomic_json(target / "manifest.json", {"schema_version": 1, "run_id": args.run_id,
                                             "state": "prepared", "created_at": dt.datetime.now(dt.UTC).isoformat(),
                                             "model": {"identity": stock, "topk": 8, "patch": None},
                                             "generation": {"gpus": [0, 1], "best_of": args.best_of,
                                                            "temperature": args.temperature, "max_new": args.max_new},
                                             "strata": {"patterns": list(PATTERNS), "domains": list(DOMAINS),
                                                         "schema_count": SCHEMA_COUNT, "recursion_rounds": 1},
                                             "contamination": contamination,
                                             "artifacts": {"seeds": "seeds.json",
                                                           "generation_checkpoints": [
                                                               "generation_records_gpu0.jsonl",
                                                               "generation_records_gpu1.jsonl"]}})
    print(target)


def execute(args: argparse.Namespace) -> None:
    target = run_dir(args.run_id)
    manifest_path, seeds_path = target / "manifest.json", target / "seeds.json"
    if not manifest_path.is_file() or not seeds_path.is_file():
        raise FileNotFoundError("run must be prepared first")
    manifest, frozen = json.loads(manifest_path.read_text()), json.loads(seeds_path.read_text())
    if manifest["state"] != "prepared":
        raise RuntimeError(f"run state must be prepared, got {manifest['state']}")
    if manifest["model"] != {"identity": verified_stock_identity(), "topk": 8, "patch": None}:
        raise RuntimeError("true-stock k8 identity in manifest changed")
    # The 500-sample production run must actually be model generated; fixture is
    # solely for CPU verification and stamps a non-training artifact.
    if args.fixture:
        records = cpu_fixture_records(frozen["seeds"])
        atomic_json(target / "fixture_validation.json", {"not_training_data": True, "records": records})
    else:
        completed = load_checkpoint_records(target, frozen["seeds"])
        todo = pending_seeds(frozen["seeds"], completed)
        print(f"[selfgen] resuming {args.run_id}: {len(completed)} completed, {len(todo)} pending", flush=True)
        if todo:
            gpu_preflight(args.run_id)
            by_gpu = {0: todo[0::2], 1: todo[1::2]}
            output: mp.Queue = mp.Queue()
            workers = [mp.Process(target=generation_worker, args=(gpu, by_gpu[gpu],
                       GenerationSpec(args.seed, args.temperature, args.max_new, args.best_of, gpu),
                       checkpoint_path(target, gpu), output))
                       for gpu in (0, 1)]
            for worker in workers:
                worker.start()
            try:
                wait_for_worker_completion(workers, output)
            finally:
                for worker in workers:
                    if worker.is_alive():
                        worker.terminate()
                    worker.join(timeout=30)
                output.close()
                output.join_thread()
        records = list(load_checkpoint_records(target, frozen["seeds"]).values())
        records.sort(key=lambda record: record["seed"]["seed_id"])
        if len(records) != len(frozen["seeds"]):
            raise RuntimeError("missing generated records")
    contamination, grams, names = contamination_corpus()
    accepted, rejected, reasons = evaluate_records(records, grams, names)
    if args.fixture:
        # Fixture proves validation only; never make it look like a corpus.
        atomic_json(target / "fixture_summary.json", {"accepted": len(accepted), "rejected": len(rejected),
                                                       "reason_counts": dict(reasons)})
        print("fixture validation passed; no training jsonl written")
        return
    atomic_jsonl(target / "train.jsonl", accepted)
    atomic_jsonl(target / "rejected.jsonl", rejected)
    summary = {"schema_version": 1, "run_id": args.run_id, "completed_at": dt.datetime.now(dt.UTC).isoformat(),
               "generated": len(records), "accepted": len(accepted), "rejected": len(rejected),
               "acceptance_rate": len(accepted) / len(records) if records else 0.0,
               "rejection_reasons": dict(reasons), "strata": {
                   "generated": {k: sum(r["seed"]["pattern"] == k for r in records) for k in PATTERNS},
                   "accepted": {k: sum(x["metadata"]["pattern"] == k for x in accepted) for k in PATTERNS}},
               "truncation_count": 0, "contamination": contamination}
    atomic_json(target / "summary.json", summary)
    manifest["state"] = "completed"
    # jobEvents' local-manifest watcher uses this established terminal marker.
    manifest["status"] = "complete"
    manifest["completed_at"] = summary["completed_at"]
    manifest["artifacts"].update({
        "generation_checkpoints": ["generation_records_gpu0.jsonl", "generation_records_gpu1.jsonl"],
        "train": "train.jsonl", "rejected": "rejected.jsonl", "summary": "summary.json"})
    atomic_json(manifest_path, manifest)
    print(canonical(summary))


def preflight(args: argparse.Namespace) -> None:
    print(json.dumps(gpu_preflight(args.run_id), ensure_ascii=False, indent=2))


def parser() -> argparse.ArgumentParser:
    p = argparse.ArgumentParser(description=__doc__)
    sub = p.add_subparsers(required=True)
    for name, func in (("prepare", prepare), ("execute", execute), ("preflight", preflight)):
        s = sub.add_parser(name)
        s.add_argument("--run-id", required=True)
        if name != "preflight":
            s.add_argument("--n", type=int, default=500)
            s.add_argument("--seed", type=int, default=20260711)
            s.add_argument("--best-of", type=int, default=4)
            s.add_argument("--temperature", type=float, default=0.7)
            s.add_argument("--max-new", type=int, default=512)
        if name == "execute":
            s.add_argument("--fixture", action="store_true")
        s.set_defaults(func=func)
    return p


if __name__ == "__main__":
    args = parser().parse_args()
    if hasattr(args, "n") and (args.n <= 0 or args.best_of != 4 or args.max_new < 32):
        raise SystemExit("require n>0, best-of=4, and max-new>=32")
    args.func(args)
