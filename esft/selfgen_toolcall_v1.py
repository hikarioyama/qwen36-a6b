def canonical(value: Any) -> str:
    return json.dumps(value, ensure_ascii=False, sort_keys=True, separators=(",", ":"))


def sha256(path: Path) -> str:
    digest = hashlib.sha256()
    with path.open("rb") as fh:
        for block in iter(lambda: fh.read(1024 * 1024), b""):
            digest.update(block)
    return digest.hexdigest()


def atomic_json(path: Path, value: Any) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    tmp = path.with_suffix(path.suffix + ".tmp")
    tmp.write_text(json.dumps(value, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
    tmp.replace(path)


def load_json_or_jsonl(path: Path) -> Any:
    """BFCL packages both JSON arrays and line-delimited .json files."""
    text = path.read_text(encoding="utf-8")
    try:
        return json.loads(text)
    except json.JSONDecodeError as array_error:
        rows = []
        try:
            for line in text.splitlines():
                if line.strip():
                    rows.append(json.loads(line))
        except json.JSONDecodeError:
            raise array_error
        if not rows:
            raise array_error
        return rows


def atomic_jsonl(path: Path, rows: Iterable[dict[str, Any]]) -> None:
    """Write a completed corpus atomically so a resumed execute cannot duplicate it."""
    path.parent.mkdir(parents=True, exist_ok=True)
    tmp = path.with_suffix(path.suffix + ".tmp")
    with tmp.open("w", encoding="utf-8") as fh:
        for row in rows:
            fh.write(canonical(row) + "\n")
        fh.flush()
        os.fsync(fh.fileno())
    tmp.replace(path)


def normal_tokens(value: Any) -> list[str]:
    return re.findall(r"[a-z0-9]+", str(value).lower().replace("_", " "))


def ngrams(value: Any, n: int = 8) -> set[tuple[str, ...]]:
    toks = normal_tokens(value)
    return {tuple(toks[i:i + n]) for i in range(max(0, len(toks) - n + 1))}


def value_for(prop: dict[str, Any], index: int) -> Any:
    if "enum" in prop:
        return prop["enum"][index % len(prop["enum"])]
    kind = prop["type"]
    if kind == "string":
        return f"item-{index + 17}"
    if kind == "integer":
        return index + 2
    if kind == "number":
        return round(index + 0.75, 2)
    if kind == "boolean":
        return bool(index % 2)
    if kind == "array":
        return [f"tag-{index + 1}", f"tag-{index + 2}"]
    raise ValueError(f"unsupported type {kind}")


def validate_value(value: Any, spec: dict[str, Any]) -> str | None:
    kind = spec.get("type")
    if kind == "string" and not isinstance(value, str):
        return "type_string"
    if kind == "integer" and (not isinstance(value, int) or isinstance(value, bool)):
        return "type_integer"
    if kind == "number" and (not isinstance(value, (int, float)) or isinstance(value, bool)):
        return "type_number"
    if kind == "boolean" and not isinstance(value, bool):
        return "type_boolean"
    if kind == "array":
        if not isinstance(value, list) or not all(isinstance(x, str) for x in value):
            return "type_array"
    if "enum" in spec and value not in spec["enum"]:
        return "enum"
    return None


def validate_call(call: Any, tools: dict[str, dict[str, Any]]) -> tuple[str | None, dict[str, Any] | None]:
    if not isinstance(call, dict) or set(call) != {"name", "arguments"}:
        return "call_shape", None
    name, args = call["name"], call["arguments"]
    if not isinstance(name, str) or name not in tools:
        return "undeclared_function", None
    if not isinstance(args, dict):
        return "arguments_not_object", None
    params = tools[name]["parameters"]
    props, required = params["properties"], set(params["required"])
    if set(args) - set(props):
        return "unknown_argument", None
    if required - set(args):
        return "missing_required", None
    for key, value in args.items():
        error = validate_value(value, props[key])
        if error:
            return f"{error}:{key}", None
    return None, {"name": name, "arguments": args}


def extract_json(text: str) -> tuple[Any | None, str | None]:
    """Accept one JSON object, optionally fenced; no repair beyond fencing."""
    stripped = text.strip()
    if stripped.startswith("```"):
        m = re.fullmatch(r"```(?:json)?\s*(.*?)\s*```", stripped, flags=re.S | re.I)
        if not m:
            return None, "json_fence"
        stripped = m.group(1)
    try:
        return json.loads(stripped), None
    except json.JSONDecodeError:
        return None, "json_parse"


def parse_model_turn(text: str, tools: dict[str, dict[str, Any]]) -> tuple[list[dict[str, Any]] | None, str | None]:
    payload, error = extract_json(text)
    if error:
        return None, error
    if not isinstance(payload, dict) or set(payload) != {"calls"} or not isinstance(payload["calls"], list):
        return None, "turn_shape"
    if not payload["calls"]:
        return None, "empty_calls"
    calls = []
    for call in payload["calls"]:
        error, normalized = validate_call(call, tools)
        if error:
            return None, error
        assert normalized is not None
        calls.append(normalized)
    if len({canonical(x) for x in calls}) != len(calls):
        return None, "duplicate_call"
    return calls, None


def bfcl_structural_profile() -> dict[str, Any]:
    """Reduce BFCL evaluation schemas to statistics; retain neither task nor API text."""
    if not BFCL_DATA.is_dir():
        return {"available": False, "reason": f"missing {BFCL_DATA}"}
    arities: list[int] = []
    type_counts: Counter[str] = Counter()
    files: dict[str, str] = {}
    for path in sorted(BFCL_DATA.glob("BFCL_v4_*.json")):
        rows = load_json_or_jsonl(path)
        if not isinstance(rows, list):
            continue
        files[str(path.relative_to(ROOT))] = sha256(path)
        for row in rows:
            if not isinstance(row, dict):
                # Some auxiliary BFCL files are scalar metadata rather than
                # evaluation records.  They contribute no schema statistics.
                continue
            for fn in row.get("function", []):
                # Function documentation is traversed only long enough to count
                # parameters/types.  Names, descriptions, and examples do not escape.
                params = fn.get("parameters", {}) if isinstance(fn, dict) else {}
                props = params.get("properties", {}) if isinstance(params, dict) else {}
                arities.append(len(props))
                for prop in props.values():
                    if isinstance(prop, dict):
                        raw = prop.get("type", "unknown")
                        type_counts[raw if isinstance(raw, str) else "union"] += 1
    return {
        "available": True,
        "method": "aggregate arity/type counts only; no BFCL content retained",
        "files_sha256": files,
        "function_count": len(arities),
        "arity_histogram": dict(sorted(Counter(arities).items())),
        "type_histogram": dict(sorted(type_counts.items())),
    }


def contamination_corpus() -> tuple[dict[str, Any], set[tuple[str, ...]], set[str]]:
    """Load local eval text solely for rejection, including ACEBench if present."""
    sources: list[Path] = []
    if BFCL_DATA.is_dir():
        sources.extend(sorted(BFCL_DATA.glob("BFCL_v4_*.json")))
    ace_roots = [p for p in ACE_CANDIDATES if p.exists()]
    for root in ace_roots:
        sources.extend(sorted(p for p in root.rglob("*") if p.suffix.lower() in {".json", ".jsonl"}))
    grams: set[tuple[str, ...]] = set()
    names: set[str] = set()
    hashes: dict[str, str] = {}
    def visit(x: Any) -> None:
        if isinstance(x, dict):
            for key, value in x.items():
                if key in {"name", "function_name"} and isinstance(value, str):
                    names.add(value.lower())
                visit(value)
        elif isinstance(x, list):
            for value in x:
                visit(value)
        elif isinstance(x, str):
            grams.update(ngrams(x))
    for path in sources:
        hashes[str(path.relative_to(ROOT))] = sha256(path)
        try:
            visit(load_json_or_jsonl(path))
        except (OSError, UnicodeDecodeError, json.JSONDecodeError):
            # A malformed local source must be visible in the manifest but cannot
            # quietly turn into copied output.
            raise RuntimeError(f"cannot parse contamination source {path}")
    return ({
        "method": "reject exact normalized 8-gram overlap or evaluator function-name collision",
        "bfcl_sources": sum(1 for p in sources if "BFCL_v4_" in p.name),
        "acebench_roots": [str(p.relative_to(ROOT)) for p in ace_roots],
        "acebench_present": bool(ace_roots),
        "source_sha256": hashes,
        "ngram_size": 8,
    }, grams, names)


def contaminated(seed: dict[str, Any], eval_grams: set[tuple[str, ...]], eval_names: set[str]) -> str | None:
    schema_names = {tool["name"].lower() for tool in seed["tools"]}
    if schema_names & eval_names:
        return "contamination_function_name"
    projection = {"request": seed["user_request"], "tools": seed["tools"]}
    if ngrams(canonical(projection)) & eval_grams:
        return "contamination_8gram"
    return None


def property_spec(slot: int, rng: random.Random) -> dict[str, Any]:
    kinds = ("string", "integer", "number", "boolean", "array")
    kind = kinds[(slot + rng.randrange(len(kinds))) % len(kinds)]
    out: dict[str, Any] = {"type": kind}
    if kind == "string" and slot % 3 == 0:
        out["enum"] = [f"mode-{slot}-a", f"mode-{slot}-b", f"mode-{slot}-c"]
    if kind == "array":
        out["items"] = {"type": "string"}
    return out


def build_tool(domain: str, seed_id: int, ordinal: int, rng: random.Random) -> dict[str, Any]:
    action = ("inspect", "list", "reserve", "update", "search", "estimate", "schedule")[ordinal % 7]
    count = 2 + ((seed_id + ordinal) % 3)
    props: dict[str, dict[str, Any]] = {}
    for j in range(count):
        props[f"field_{ordinal + 1}_{j + 1}"] = property_spec(seed_id + j + ordinal, rng)
    required = list(props)[:2 + ((seed_id + ordinal) % max(1, count - 1))]
    return {
        "type": "function",
        "name": f"mock_{domain}_{seed_id:03d}_{action}_{ordinal + 1}",
        "description": f"Synthetic {domain} operation {ordinal + 1}; offline mock only.",
        "parameters": {"type": "object", "properties": props, "required": required,
                       "additionalProperties": False},
    }


def require_link_field(tool: dict[str, Any], name: str, spec: dict[str, Any]) -> None:
    """Make a mock-executor result dependency explicit in a follow-up schema."""
    params = tool["parameters"]
    params["properties"][name] = spec
    params["required"].append(name)


def expected_call(tool: dict[str, Any], nonce: int) -> dict[str, Any]:
    props = tool["parameters"]["properties"]
    args = {name: value_for(spec, nonce + pos) for pos, (name, spec) in enumerate(props.items())
            if name in tool["parameters"]["required"]}
    return {"name": tool["name"], "arguments": args}


def call_request_fragment(call: dict[str, Any], *, omit: set[str] | None = None) -> str:
    omit = omit or set()
    values = "; ".join(f"{key}={canonical(value)}" for key, value in call["arguments"].items()
                       if key not in omit)
    return f"call {call['name']} with {values}"


def make_seed(seed_id: int, rng: random.Random) -> dict[str, Any]:
    schema_id = seed_id % SCHEMA_COUNT
    domain = DOMAINS[schema_id % len(DOMAINS)]
    pattern = PATTERNS[seed_id % len(PATTERNS)]
    tools = [build_tool(domain, schema_id, i, rng) for i in range(3)]
    if pattern == "single":
        stages = [[expected_call(tools[0], seed_id)]]
        request = f"For this {domain} request, {call_request_fragment(stages[0][0])}."
    elif pattern == "parallel":
        stages = [[expected_call(tools[0], seed_id), expected_call(tools[1], seed_id + 11)]]
        request = (f"For this {domain} request, independently and in parallel, "
                   f"{call_request_fragment(stages[0][0])}; also {call_request_fragment(stages[0][1])}.")
    elif pattern == "multi_turn":
        first = expected_call(tools[0], seed_id)
        receipt = mock_execute(first, 0, pattern)["result"]["receipt"]
        require_link_field(tools[1], "prior_receipt", {"type": "string"})
        second = expected_call(tools[1], seed_id + 19)
        second["arguments"]["prior_receipt"] = receipt
        stages = [[first], [second]]
        request = (f"For this {domain} request, first {call_request_fragment(first)}. After its tool result, "
                   f"{call_request_fragment(second, omit={'prior_receipt'})}, and set prior_receipt to the "
                   "receipt returned by the first tool.")
    else:
        first = expected_call(tools[0], seed_id)
        require_link_field(tools[2], "recovery_code", {"type": "string", "enum": ["UNAVAILABLE"]})
        second = expected_call(tools[2], seed_id + 29)
        second["arguments"]["recovery_code"] = "UNAVAILABLE"
        stages = [[first], [second]]
        request = (f"For this {domain} request, first {call_request_fragment(first)}. If it returns an error, "
                   f"recover by {call_request_fragment(second, omit={'recovery_code'})} and set recovery_code "
                   "to the returned error code.")
    return {"seed_id": f"seed-{seed_id:04d}", "schema_id": f"schema-{schema_id:03d}",
            "domain": domain, "pattern": pattern,
            "tools": tools, "user_request": request, "expected_stages": stages}


def build_seeds(count: int, rng_seed: int, profile: dict[str, Any],
                eval_grams: set[tuple[str, ...]], eval_names: set[str]) -> tuple[list[dict[str, Any]], Counter[str]]:
    # Domain/pattern are round-robin: requested strata are balanced before model output.
    rng = random.Random(rng_seed)
    seeds, rejected = [], Counter()
    candidate = 0
    while len(seeds) < count:
        seed = make_seed(candidate, rng)
        reason = contaminated(seed, eval_grams, eval_names)
        if reason:
            rejected[reason] += 1
        else:
            seed["structure_reference"] = {
                "bfcl_function_count": profile.get("function_count", 0),
                "bfcl_arity_histogram": profile.get("arity_histogram", {}),
            }
            seeds.append(seed)
        candidate += 1
        if candidate > count * 20:
            raise RuntimeError("contamination filter excluded too many generated seeds")
    return seeds, rejected


def mock_execute(call: dict[str, Any], stage: int, pattern: str) -> dict[str, Any]:
    """Deterministic API result; executor is never allowed to make network calls."""
    if pattern == "error_recovery" and stage == 0:
        return {"ok": False, "error": {"code": "UNAVAILABLE", "retryable": True},
                "call": call["name"]}
    digest = hashlib.sha256(canonical(call).encode()).hexdigest()[:12]
    return {"ok": True, "result": {"receipt": f"mock-{digest}", "stage": stage + 1},
            "call": call["name"]}


